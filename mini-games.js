function quizGame() {

    const quiz = [
        {
            question: "Какой цвет неба?",
            options: ["1. Красный", "2. Синий", "3. Зеленый"],
            correctAnswer: 2 
        },
        {
            question: "Сколько дней в неделе?",
            options: ["1. Шесть", "2. Семь", "3. Восемь"],
            correctAnswer: 2
        },
        {
            question: "Сколько у человека пальцев на одной руке?",
            options: ["1. Четыре", "2. Пять", "3. Шесть"],
            correctAnswer: 2
        }
    ];

    let score = 0;
    
    for (let i = 0; i < quiz.length; i++) {
        const question = quiz[i].question;
        const options = quiz[i].options.join("\n");
        const userAnswer = prompt(`${question}\n${options}`);
        const answerIndex = parseInt(userAnswer);

        if (userAnswer === null) {
            alert('Игра завершена. Попробуйте снова!');
            return; 
        }

        if (answerIndex === quiz[i].correctAnswer) {
            score++; 
        }
    }

    alert(`Вы ответили правильно на ${score} из ${quiz.length} вопросов.`);
}

