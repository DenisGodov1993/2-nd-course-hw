function rockScissorsPaper() {
    
    const userChoice = prompt('Камень, ножницы или бумага?');
   
    if (userChoice === null) {
        alert('Отмена игры. Попробуйте снова!');
        return; 
    }

    const choices = ["камень", "ножницы", "бумага"];

    if (!choices.includes(userChoice)) {
        alert('Ошибка! Некорректный ввод! Попробуйте снова.');
        return rockScissorsPaper(); 
    }

    const computerChoice = choices[Math.floor(Math.random() * choices.length)];

    let result;
    if (userChoice === computerChoice) {
        result = 'Ничья! Победила дружба!';
    } else if (
        (userChoice === 'камень' && computerChoice === 'ножницы') ||
        (userChoice === 'ножницы' && computerChoice === 'бумага') ||
        (userChoice === 'бумага' && computerChoice === 'камень')
    ) {
        result = 'Поздравляем! Вы победили!';
    } else {
        result = 'Вы проиграли. Попробуйте снова!'; 
    }

    alert(`Ваш выбор: ${userChoice}\nВыбор компьютера: ${computerChoice}\nРезультат: ${result}`);
}

