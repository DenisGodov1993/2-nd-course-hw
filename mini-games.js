const operators = ['+', '-', '*', '/'];
let randomTask;
let correctAnswer;

function simpleArithmetic() {
    const operators = ['+', '-', '*', '/'];
    const num1 = Math.floor(Math.random() * 10) + 1; 
    const num2 = Math.floor(Math.random() * 10) + 1;
    const operator = operators[Math.floor(Math.random() * operators.length)];
    let randomTask = `${num1} ${operator} ${num2}`; 
    const userAnswer = prompt(`Решите задачу: ${randomTask}`);
    const userAnswerNumber = Number(userAnswer);
    
    switch (operator) {
        case '+':
            correctAnswer = num1 + num2;
            break;
        case '-':
            correctAnswer = num1 - num2;
            break;
        case '*':
            correctAnswer = num1 * num2;
            break;
        case '/':
            correctAnswer = num1 / num2;
            break;
    }

    if (userAnswer === null) {
        alert(`Игра завершeна! Правильный ответ был: ${correctAnswer}. Попробуйте снова.`);
        return;
    }
    
    if (isNaN(userAnswerNumber)) {
        alert('Ошибка! Некорректный ввод! Попробуйте снова.');
        return;
    }

    if (userAnswerNumber === correctAnswer) {
        alert('Поздравляем!!! Правильный ответ!');
    } else {
        alert(`Ответ неправильный. Попробуйте снова!`);
    }
}

