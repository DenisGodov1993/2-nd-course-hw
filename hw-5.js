// Создайте функцию, которая:
// 1.Спрашивает у пользователя, сколько ему лет.
// 2.Если пользователь введет отрицательное число — выведет на экран 
// 'Вы ввели неправильное значение'.
// 3.Если пользователь введет число от 0 до 12 — выведет на экран 'Привет, друг!'.
// 4.Если пользователь введет число больше или равно 13 — выведет на экран 
// 'Добро пожаловать!'.
// Подсказка: можете выполнить это задание с помощью оператора if else.

function age() {
    let age = prompt('Сколько теле лет?');

    if (age < 0) {
        alert('Вы ввели неправильное значение');
    } else if (age >= 0 && age <= 12) {
        alert('Привет, друг!');
    } else if (age >= 13) {
        alert('Добро пожаловать!');
    } else {
        alert('Вы ввели неправильное значение');
    }
}
age();

