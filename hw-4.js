// Есть объект (назовем его obj), который содержит следующие пары ключ-значение:
// "Коля" : '200'
// "Вася" : '300'
// "Петя" : '400'
// Эти пары представляют собой имена сотрудников и их зарплаты в долларах. 
// Ваша задача написать код, который будет проходиться по всем записям этого объекта и выводить 
// информацию о каждом сотруднике в формате: "имя сотрудника" — зарплата "сумма" долларов.
// Для итерации по объекту используйте цикл for-in.

const obj = {
    Коля: '200',
    Вася : '300',
    Петя : '400'
}
for (let name in obj) {
    console.log(`имя сотрудника ${name} - зарплата ${obj[name]} долларов`);
}

// Задание 5
// Дано число n=1000. Ваша задача — делить его на 2 до тех пор, пока результат деления 
// не станет меньше 50.
// 1 Определите, какое число получится в результате.
// 2 Вычислите количество итераций (проходов цикла), которые потребовались для достижения 
// результата, и запишите это количество в переменную num.

