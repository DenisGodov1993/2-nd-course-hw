// -считает дни месяца по датам,
// -определяет, какой день пятница,
// -выводит сообщение с напоминанием, что нужно подготовить еженедельный отчет.
// Условия задачи:
// 1 Создайте переменную, которая хранит в себе номер первой пятницы месяца (число от 1 до 7).
// 2 Выведите на каждую пятницу месяца (включая полученную) сообщение следующего вида: 
// "Сегодня пятница, ...-е число. Необходимо подготовить отчет."
// 3 Исходите из того, что в нашем месяце 31 день. Должно вывестись от 4 до 5 сообщений 
